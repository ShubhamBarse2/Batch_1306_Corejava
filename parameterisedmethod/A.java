package com.parameterisedmethod;

public class A {

	public void myAddition(int a, int b) {

		System.out.println(a + b);

	}

	public void mySub(int a, int b) {

		System.out.println(a - b);

	}

	public void myMul(int a, int b) {

		System.out.println(a * b);

	}

	public void myDiv(int a, int b) {

		System.out.println(a / b);

	}

}
